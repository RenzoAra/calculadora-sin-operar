export class Calc {
    static sumar(a: number, b: number) : number {
        return (a+b);
    }
    static restar (a:number, b:number) : number{
        return (a-b);
    }
    static dividir(a:number, b:number): number{
        return (a/b);
    }
    static multiplicar(a:number, b:number): number{
        return (a*b);
    }
}